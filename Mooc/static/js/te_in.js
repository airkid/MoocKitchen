$(document).ready(function(){
        $("#show_news").click(function(){
          $("#show_news").addClass("active");
          $("#show_test").removeClass("active");
          $("#show_ppt").removeClass("active");
          $("#show_talk").removeClass("active");
          $("#test").hide();
          $("#ppt").hide();
          $("#talk").hide();
          $("#news").show();
        });
        $("#show_test").click(function(){
          $("#show_test").addClass("active");
          $("#show_news").removeClass("active");
          $("#show_ppt").removeClass("active");
          $("#show_talk").removeClass("active");
          $("#news").hide();
          $("#ppt").hide();
          $("#talk").hide();
          $("#test").show();
        });
        $("#show_ppt").click(function(){
          $("#show_ppt").addClass("active");
          $("#show_news").removeClass("active");
          $("#show_test").removeClass("active");
          $("#show_talk").removeClass("active");
          $("#news").hide();
          $("#test").hide();
          $("#talk").hide();
          $("#ppt").show();
        });
        $("#show_talk").click(function(){
          $("#show_talk").addClass("active");
          $("#show_news").removeClass("active");
          $("#show_test").removeClass("active");
          $("#show_ppt").removeClass("active");
          $("#news").hide();
          $("#test").hide();
          $("#ppt").hide();
          $("#talk").show();
        });
      });